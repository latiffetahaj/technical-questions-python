import unittest
from __9__valid_sudoku import isValidSudoku

class TestIsValidSudoku(unittest.TestCase):
    def test_true(self):
        self.assertTrue(isValidSudoku(  [["8","3",".",".","7",".",".",".","."]
                                        ,["6",".",".","1","9","5",".",".","."]
                                        ,[".","9",".",".",".",".",".","6","."]
                                        ,[".",".",".",".","6",".",".",".","3"]
                                        ,["4",".",".","8",".","3",".",".","1"]
                                        ,["7",".",".",".","2",".",".",".","6"]
                                        ,[".","6",".",".",".",".","2","8","."]
                                        ,[".",".",".","4","1","9",".",".","5"]
                                        ,[".",".",".",".","8",".",".","7","9"]]))

        self.assertTrue(isValidSudoku(  [["5",".",".",".","2","7",".",".","1"]
                                        ,["8",".",".",".",".",".",".","7","5"]
                                        ,["6",".","2",".","3",".","9","4","."]
                                        ,["1","5",".","4","9",".",".",".","3"]
                                        ,[".","8",".","7",".",".",".",".","9"]
                                        ,[".",".",".","2","1","8",".",".","."]
                                        ,["4",".",".","9",".","2",".",".","7"]
                                        ,["9","2","8","3",".",".",".","1","6"]
                                        ,[".","6","3","1","8","5",".",".","."]]))


    def test_false(self):
        self.assertFalse(isValidSudoku([["8","3",".",".","7",".",".",".","."],
                                        ["6",".",".","1","9","5",".",".","."],
                                        [".","9","8",".",".",".",".","6","."],
                                        ["8",".",".",".","6",".",".",".","3"],
                                        ["4",".",".","8",".","3",".",".","1"],
                                        ["7",".",".",".","2",".",".",".","6"],
                                        [".","6",".",".",".",".","2","8","."],
                                        [".",".",".","4","1","9",".",".","5"],
                                        [".",".",".",".","8",".",".","7","9"]]))

        self.assertFalse(isValidSudoku([["1","3",".",".","7",".",".",".","."],
                                        ["9",".",".","1","9","6",".",".","."],
                                        [".","9","8",".",".",".",".","6","."],
                                        ["7",".",".",".","6",".",".",".","3"],
                                        ["3",".",".","8",".","5",".",".","9"],
                                        ["6",".",".",".","2",".",".",".","8"],
                                        [".","6",".",".",".",".","2","8","."],
                                        [".",".",".","4","1","9",".",".","5"],
                                        [".",".",".",".","8",".",".","7","1"]]))


if __name__ == '__main__':
    unittest.main()